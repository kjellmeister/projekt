Ankeborgs veckoblad kan vara en av de mest omtyckta tidningarna i Ankeborg.</br></br>
Vi skriver om allt som händer i vår vackra stad och lite till.</br></br>

Hemsidan är gjord av Johannes Kjellgren, som är den bäste webbprogrammeraren som vi någonsinn har haft att göra med!
Precis alla krav som vi hade på hemsidan blev uppfyllda! Och lite till!</br></br>

Välkommen hit! Hoppas att du finner intressanta nyheter på vår underbara hemsida.</br></br>

Peace out homie G</br></br>
